def test_config() -> None:
    _section("1 · Config & Environment")
    missing = config.validate()
    if missing:
        results["config"] = (FAIL, f"Missing: {', '.join(missing)}")
        print(f"  {FAIL}  Missing keys: {missing}")
        return

    checks = {
        "bot_token":    bool(config.bot_token),
        "sheet_id":     bool(config.google_sheet_id),
        "fb_location":  bool(config.fb_search_location),
        "creds_file":   Path(config.google_credentials_file).exists(),
        "dry_run=false": not config.dry_run,
        "keywords":     len(config.fb_search_keywords) > 0,
        "chat_ids":     len(config.authorized_chat_ids) > 0,
        "team_ids":     len(config.team_chat_ids) > 0,
    }
    failed = [k for k, v in checks.items() if not v]

    if failed:
        results["config"] = (FAIL, f"Failed checks: {', '.join(failed)}")
        print(f"  {FAIL}  {', '.join(failed)}")
    else:
        results["config"] = (PASS, "All required config present")
        print(f"  {PASS}  All config present")

    print(f"  dry_run   : {config.dry_run}")
    print(f"  location  : {config.fb_search_location}")
    print(f"  keywords  : {config.fb_search_keywords}")
    print(f"  sheet_id  : {'set' if config.google_sheet_id else 'MISSING'}")
    print(f"  chat_ids  : {len(config.authorized_chat_ids)} authorized, {len(config.team_chat_ids)} team")


# ─────────────────────────────────────────────────────────────────────────────
# 2. GOOGLE SHEETS
# ─────────────────────────────────────────────────────────────────────────────

def test_sheets() -> str | None:
    """Returns the lead_id of the test lead if written, so we can clean up."""
    _section("2 · Google Sheets (live read + write + delete)")
    from integrations.sheets import SheetsClient, COLUMNS

    test_lead_id = None
    try:
        sc = SheetsClient()
        print("  Connected to Google Sheets ✓")

        # Write a clearly-labelled test lead
        test_payload = {
            "name":           "TEST_LEAD — auto-generated by test_full_system.py",
            "contact":        "test@haulyeah.test",
            "listing_url":    "https://www.facebook.com/marketplace/item/TEST000000/",
            "job_type":       "TEST junk removal",
            "description":    "System test entry — safe to delete. Generated by test_full_system.py.",
            "location":       config.fb_search_location,
            "urgency_score":  1,
            "size_score":     1,
        }
        test_lead_id = sc.add_lead(test_payload)
        print(f"  Lead written  → id={test_lead_id}")

        # Read it back
        lead = sc.get_lead_by_id(test_lead_id)
        if not lead:
            results["sheets"] = (FAIL, "Lead written but not readable")
            print(f"  {FAIL}  Lead not found after write")
            return test_lead_id

        print(f"  Lead read back → status={lead.get('status')} ✓")

        # Sanity check: status should be "new"
        if lead.get("status") != "new":
            results["sheets"] = (FAIL, f"Unexpected status: {lead.get('status')}")
            return test_lead_id

        # Count existing leads
        new_count = len(sc.get_leads_by_status("new"))
        print(f"  Total 'new' leads in sheet: {new_count}")

        results["sheets"] = (PASS, f"Write + read confirmed (id={test_lead_id})")
        print(f"  {PASS}  Sheets read/write working")
        return test_lead_id

    except Exception as exc:
        results["sheets"] = (FAIL, str(exc))
        print(f"  {FAIL}  {exc}")
        return test_lead_id


def cleanup_test_lead(lead_id: str | None) -> None:
    """Mark the test lead 'declined' so it's clearly swept up later."""
    if not lead_id:
        return
    try:
        from integrations.sheets import SheetsClient
        sc = SheetsClient()
        sc.update_lead(lead_id, {"status": "declined", "response_notes": "AUTO-DELETED by test_full_system.py"})
        print(f"\n  [cleanup] Test lead {lead_id} marked declined ✓")
    except Exception as exc:
        print(f"\n  [cleanup] Could not clean up {lead_id}: {exc}")


