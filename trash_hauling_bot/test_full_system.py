async def run_all() -> None:
    print(f"\n{'='*60}")
    print(f"  HaulYeah — Full System Test")
    print(f"  {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print(f"{'='*60}")

    test_config()

    # Stop here if config is broken — nothing else will work
    if results.get("config", (FAIL,))[0] == FAIL:
        print("\n  Config failed — skipping remaining tests.")
        print_summary()
        return

    test_lead_id = test_sheets()
    test_outreach()
    await test_telegram()
    await test_fb_session()

    cleanup_test_lead(test_lead_id)
    print_summary()


if __name__ == "__main__":
    asyncio.run(run_all())
