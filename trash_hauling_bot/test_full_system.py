def print_summary() -> None:
    _section("SUMMARY")
    order = ["config", "sheets", "outreach", "telegram", "fb_session"]
    all_pass = True
    for key in order:
        status, detail = results.get(key, (SKIP, "not run"))
        print(f"  {status}  {key:<12}  {detail}")
        if status == FAIL:
            all_pass = False

    print()
    if all_pass:
        print("  🎉  All tests passed — HaulYeah is fully operational!")
    else:
        print("  ⚠️   Some tests failed — see above for details.")

    # Write results file
    out = Path(config.data_dir) / "test_full_system_results.txt"
    out.parent.mkdir(exist_ok=True)
    with open(out, "w", encoding="utf-8") as f:
        f.write(f"HaulYeah Full System Test — {datetime.now()}\n\n")
        for key in order:
            status, detail = results.get(key, (SKIP, "not run"))
            f.write(f"{status}  {key}: {detail}\n")
    print(f"\n  Results saved → {out}")


