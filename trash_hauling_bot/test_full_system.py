# 4. TELEGRAM BOT PING
# ─────────────────────────────────────────────────────────────────────────────

async def test_telegram() -> None:
    _section("4 · Telegram Bot Ping")
    if not config.bot_token:
        results["telegram"] = (FAIL, "TRASH_BOT_TOKEN not set")
        print(f"  {FAIL}  No bot token")
        return
    if not config.team_chat_ids:
        results["telegram"] = (SKIP, "TRASH_BOT_TEAM_CHAT_IDS empty — no chat to ping")
        print(f"  {SKIP}  TRASH_BOT_TEAM_CHAT_IDS is empty; set it to enable notifications")
        return

    try:
        from telegram import Bot
        bot = Bot(token=config.bot_token)
        me = await bot.get_me()
        print(f"  Bot identity : @{me.username} (id={me.id}) ✓")

        ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        msg = (
            f"🟢 *HaulYeah System Test — {ts}*\n"
            f"All components under test. This is an automated ping from `test_full_system.py`.\n"
            f"If you see this, Telegram notifications are working."
        )
        for chat_id in config.team_chat_ids:
            sent = await bot.send_message(chat_id=chat_id, text=msg, parse_mode="Markdown")
            print(f"  Message sent  → chat_id={chat_id} msg_id={sent.message_id} ✓")

        results["telegram"] = (PASS, f"Pinged {len(config.team_chat_ids)} chat(s)")
        print(f"  {PASS}  Telegram working")

    except Exception as exc:
        results["telegram"] = (FAIL, str(exc))
        print(f"  {FAIL}  {exc}")


