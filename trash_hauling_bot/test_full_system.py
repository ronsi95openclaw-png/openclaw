# 5. FACEBOOK SESSION CHECK
# ─────────────────────────────────────────────────────────────────────────────

async def test_fb_session() -> None:
    _section("5 · Facebook Marketplace Session")
    profile_dir = Path(config.fb_profile_dir)

    if not profile_dir.exists() or not any(profile_dir.iterdir()):
        results["fb_session"] = (SKIP, "No FB profile dir — run: python -m agents.scraper --login")
        print(f"  {SKIP}  FB profile not found at {profile_dir}")
        print("         Run: python -m agents.scraper --login")
        return

    print(f"  FB profile exists: {profile_dir} ✓")

    # Quick Playwright check: does FB redirect to login?
    try:
        from playwright.async_api import async_playwright
        from agents.scraper import _USER_AGENT

        async with async_playwright() as pw:
            ctx = await pw.chromium.launch_persistent_context(
                str(profile_dir),
                headless=True,
                channel="chrome",
                args=["--no-sandbox", "--disable-blink-features=AutomationControlled"],
                user_agent=_USER_AGENT,
            )
            page = await ctx.new_page()
            await page.set_extra_http_headers({"Accept-Language": "en-US,en;q=0.9"})

            test_url = (
                "https://www.facebook.com/marketplace/search/"
                "?query=junk+removal&sortBy=creation_time_descend"
            )
            print(f"  Loading FB Marketplace (headless)…")
            await page.goto(test_url, timeout=30_000, wait_until="domcontentloaded")

            import asyncio as _asyncio
            await _asyncio.sleep(2.5)

            final_url = page.url
            has_login_form = await page.query_selector('input[name="email"]')

            if "login" in final_url.lower() or has_login_form:
                results["fb_session"] = (FAIL, "FB login wall — session expired. Run --login")
                print(f"  {FAIL}  FB redirected to login. Session cookie expired.")
                print("         Fix: python -m agents.scraper --login")
            else:
                # Count listings visible
                links = await page.query_selector_all('a[href*="/marketplace/item/"]')
                results["fb_session"] = (PASS, f"Logged in, found {len(links)} listing links")
                print(f"  {PASS}  FB session valid — {len(links)} listing link(s) visible")

            await ctx.close()

    except Exception as exc:
        results["fb_session"] = (FAIL, str(exc))
        print(f"  {FAIL}  Playwright error: {exc}")


